import * as fs from 'node:fs';
import * as path from 'node:path';

export interface OpenApiSpec {
  paths: Record<string, Record<string, Operation>>;
}

interface Operation {
  operationId?: string;
  summary?: string;
  requestBody?: {
    content?: {
      'application/json'?: {
        schema?: JsonSchema;
      };
    };
  };
}

interface JsonSchema {
  properties?: Record<string, JsonSchema>;
  required?: string[];
  items?: JsonSchema;
  type?: string;
  enum?: string[];
  description?: string;
  'x-positional-index'?: number;
}

interface GeneratedField {
  type?: string;
  enum?: string[];
  description?: string;
  positionalIndex?: number;
}

export interface GeneratedRoute {
  operationId?: string;
  summary?: string;
  route: {
    tool: string;
    action: string;
    method: 'GET' | 'POST';
  };
  required?: string[];
  schema?: Record<string, GeneratedField>;
}

const root = path.join(import.meta.dir, '..');
const openApiPath = path.join(root, 'spacemolt-docs', 'openapi.json');
const outputPath = path.join(root, 'src', 'generated', 'api-commands.ts');

function routeParts(apiPath: string): { tool: string; action: string } {
  const parts = apiPath.replace(/^\/api\/v2\/?/, '').split('/');
  const tool = parts[0];
  if (!tool) throw new Error(`Invalid API path: ${apiPath}`);
  return {
    tool,
    action: parts[1] || tool,
  };
}

function fieldSchema(schema: JsonSchema): GeneratedField {
  const field: GeneratedField = {};
  if (schema.type) field.type = schema.type;
  if (schema.enum) field.enum = schema.enum;
  else if (schema.items?.enum) field.enum = schema.items.enum;
  if (schema.description) field.description = schema.description;
  if (schema['x-positional-index'] !== undefined) field.positionalIndex = schema['x-positional-index'];
  return field;
}

function requestSchema(operation: Operation): Pick<GeneratedRoute, 'required' | 'schema'> {
  const schema = operation.requestBody?.content?.['application/json']?.schema;
  if (!schema?.properties) return {};

  return {
    required: schema.required,
    schema: Object.fromEntries(
      Object.entries(schema.properties)
        .map(([name, property]) => [name, fieldSchema(property)] as const)
        .sort(([a], [b]) => a.localeCompare(b)),
    ),
  };
}

export function generate(spec: OpenApiSpec): Record<string, GeneratedRoute> {
  const routes: Record<string, GeneratedRoute> = {};

  for (const [apiPath, methods] of Object.entries(spec.paths).sort(([a], [b]) => a.localeCompare(b))) {
    for (const method of ['get', 'post'] as const) {
      const operation = methods[method];
      if (!operation) continue;
      const route = routeParts(apiPath);
      const generated: GeneratedRoute = {
        operationId: operation.operationId,
        summary: operation.summary,
        route: {
          ...route,
          method: method.toUpperCase() as 'GET' | 'POST',
        },
        ...requestSchema(operation),
      };
      routes[`${generated.route.method} ${apiPath}`] = generated;
    }
  }

  return routes;
}

function render(routes: Record<string, GeneratedRoute>): string {
  return `// Generated by scripts/generate-api-metadata.ts from spacemolt-docs/openapi.json.
// Do not edit by hand.

export interface GeneratedApiField {
  type?: string;
  enum?: string[];
  description?: string;
  positionalIndex?: number;
}

export interface GeneratedApiRoute {
  operationId?: string;
  summary?: string;
  route: {
    tool: string;
    action: string;
    method: 'GET' | 'POST';
  };
  required?: string[];
  schema?: Record<string, GeneratedApiField>;
}

export const GENERATED_API_ROUTES: Record<string, GeneratedApiRoute> = ${JSON.stringify(routes, null, 2)};
`;
}

if (import.meta.main) {
  const spec = JSON.parse(fs.readFileSync(openApiPath, 'utf-8')) as OpenApiSpec;
  fs.mkdirSync(path.dirname(outputPath), { recursive: true });
  fs.writeFileSync(outputPath, render(generate(spec)));
}
